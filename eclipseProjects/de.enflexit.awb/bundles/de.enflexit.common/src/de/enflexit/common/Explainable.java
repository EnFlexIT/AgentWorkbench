package de.enflexit.common;

/**
 * The Interface Explainable can be used to implement a method that 
 * explains the purpose and goal of the implementing class.
 *
 * @author Christian Derksen - SOFTEC - ICB - University of Duisburg-Essen
 */
public interface Explainable {

	/**
	 * Has to return the description of the class.
	 * @return the description
	 */
	public String getDescription();
	
}
